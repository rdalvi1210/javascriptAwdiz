// Operators

// ==
console.log("10" == 2);
console.log("10" == "10");
console.log("10" == 10);

//===
console.log("10" === 2);
console.log("10" === 10);
console.log(10 === 10);

// > greater than
console.log(2 > 1);
console.log(2 > 10);

// <  lesser than
console.log(2 < 1);
console.log(2 < 10);

//greater than equal
console.log(2 >= 2);
console.log(20 >= 21);
console.log(20 >= 10);

//lesser than equal
console.log(2 <= 2);
console.log(20 <= 19);
console.log(20 <= 21);

// AND operator
console.log(2 > 1 && 3 > 2 && 4 > 3 && 5 > 4);
console.log(2 < 1 && 3 > 2 && 4 > 3 && 5 > 4);

// OR operator
console.log(2 > 10 || 3 > 20 || 2 < 10);
console.log(2 > 10 || 3 > 20 || 2 > 10);
